let x = 10;
console.log("initial value of x:", x);

//using +=
x += 5;
console.log("After x += 5: ", x);

//using -=
x -= 5;
console.log("After x -= 5: ", x);

//using *=
x*=5;
console.log("After x *= 5: ", x);

//using /=
x /= 5;
console.log("After x /= 5: ", x);

//using %=
x %= 5;
console.log("After x %= 5: ", x);
